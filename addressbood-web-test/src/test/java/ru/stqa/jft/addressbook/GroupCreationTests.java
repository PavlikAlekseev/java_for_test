package ru.stqa.jft.addressbook;// Generated by Selenium IDE

import org.junit.Test;
import org.junit.Before;
import org.junit.After;

import static org.hamcrest.CoreMatchers.is;
import static org.hamcrest.core.IsNot.not;

import org.openqa.selenium.By;
import org.openqa.selenium.WebDriver;
import org.openqa.selenium.chrome.ChromeDriver;
import org.openqa.selenium.Dimension;
import org.openqa.selenium.JavascriptExecutor;

import java.util.*;

public class GroupCreationTests {
    private WebDriver driver;
    private Map<String, Object> vars;
    JavascriptExecutor js;

    @Before
    public void setUp() {
        driver = new ChromeDriver();
        js = (JavascriptExecutor) driver;
        vars = new HashMap<String, Object>();
        login("admin", "secret");
    }

    //метод login
    private void login(String username, String password) {
        driver.get("http://localhost/addressbook/");
        driver.manage().window().setSize(new Dimension(1294, 816));
        driver.findElement(By.name("user")).sendKeys(username);
        driver.findElement(By.name("pass")).click();
        driver.findElement(By.name("pass")).sendKeys(password);
        driver.findElement(By.cssSelector("input:nth-child(7)")).click();
    }

    @After
    public void tearDown() {
        driver.quit();
    }

    @Test
    public void testCreateGroup() {
        goToGroupPage();
        initGroupCreation();
        fillGroupForm(new GroupData("Test2", "Test", "Test"));
        submitGroupCreation();
        returnToGroupPage();
    }

    //метод возвращения на страницу Группы
    private void returnToGroupPage() {
        driver.findElement(By.linkText("group page")).click();
    }

    //метод подтверждения создания группы
    private void submitGroupCreation() {
        driver.findElement(By.name("submit")).click();
    }

    //метод принимает объект GroupData
    private void fillGroupForm(GroupData groupData) {
        driver.findElement(By.name("group_name")).click();
        driver.findElement(By.name("group_name")).sendKeys(groupData.name());
        driver.findElement(By.name("group_header")).click();
        driver.findElement(By.name("group_header")).sendKeys(groupData.header());
        driver.findElement(By.name("group_footer")).click();
        driver.findElement(By.name("group_footer")).sendKeys(groupData.footer());
    }

    //метод инициализации создания группы
    private void initGroupCreation() {
        driver.findElement(By.name("new")).click();
    }

    //метод перехода на страницу Группы
    private void goToGroupPage() {
        driver.findElement(By.linkText("groups")).click();
    }
}
